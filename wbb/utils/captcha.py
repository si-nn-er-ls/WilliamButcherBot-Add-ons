def generate_rnd_id():
    """Generate Random ID"""
    return str(uuid.uuid4().hex)


def make_captcha_markup(markup, __emoji, indicator):
    __markup = markup
    for i in markup:
        for k in i:
            if k.text == __emoji:
                k.text = f"{indicator}"
                k.callback_data = "HeHe"
                return __markup


